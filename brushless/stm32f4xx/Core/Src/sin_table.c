float sin_table(float theta){
  bool greater_pi_4;
  theta = mod_float(theta, (2*M_PI));
  if(theta>M_PI_2){
    theta = M_PI - theta;
  }
  if(theta<=-M_PI_2){
    theta = -M_PI - theta;
  }
  float sign = 1;
  if( theta < 0 ){
    theta = -theta;
    sign = -1;
  }

  // sin(2*t) = 1 - 2 sin^2(pi/4 - t)
  // sin(T) = 1 - 2 sin^2(pi/4 - T/2)
  // sin(T) = 1 - 2 sin^2( (pi/2 - T)/2 )
  if( (greater_pi_4 = (theta >=M_PI_4)) ){
    theta = (M_PI_2 - theta)/2;
  }
  const int index = (int)(theta * ((SIN_RESOLUTION)/M_PI_4));
  ASSERT( 0 <= index );
  ASSERT( index < SIN_NB_ELEM );
  if( greater_pi_4 ){
    theta = sin_samples[index];
    return sign*(1 - 2*theta*theta);
  }else{
    return sign*sin_samples[index];
  }
}
